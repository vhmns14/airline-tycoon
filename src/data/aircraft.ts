export function manufacturers(): ManufacturerInfo[] {
  const map = new Map<string, Aircraft[]>()
  for (const a of aircraft) {
    const list = map.get(a.manufacturer) ?? []
    list.push(a)
    map.set(a.manufacturer, list)
  }
  const names = [...map.keys()].sort((a, b) => {
    const ia = MAKER_ORDER.indexOf(a)
    const ib = MAKER_ORDER.indexOf(b)
    if (ia === -1 && ib === -1) return a.localeCompare(b)
    if (ia === -1) return 1
    if (ib === -1) return -1
    return ia - ib
  })
  return names.map((name) => {
    const list = map.get(name)!
    const meta = MAKER_META[name] ?? { country: '—', color: '#64748b' }
    return {
      name,
      count: list.length,
      pax: list.filter((x) => x.role === 'passenger').length,
      cargo: list.filter((x) => x.role === 'cargo').length,
      minPrice: Math.min(...list.map((x) => x.price)),
      country: meta.country,
      color: meta.color,
    }
  })
}

export function byManufacturer(name: string): Aircraft[] {
  return aircraft
    .filter((a) => a.manufacturer === name)
    .slice()
    .sort((a, b) => a.capacity - b.capacity || a.price - b.price)
}

export function passengerCatalog(): Aircraft[] {
  return aircraft
    .filter((a) => a.role === 'passenger')
    .slice()
    .sort((a, b) => a.capacity - b.capacity || a.price - b.price)
}

export function cargoCatalog(): Aircraft[] {
  return aircraft
    .filter((a) => a.role === 'cargo')
    .slice()
    .sort((a, b) => a.capacity - b.capacity || a.price - b.price)
}

/** Small pioneer / bush props (for UI filters later). */
export function pioneerCatalog(): Aircraft[] {
  return aircraft.filter(
    (a) =>
      a.minAirportSize === 1 &&
      (a.bodyClass === 'light' || a.bodyClass === 'turboprop') &&
      a.capacity <= 50 &&
      (a.role === 'cargo' ? a.capacity <= 10 : true),
  )
}
